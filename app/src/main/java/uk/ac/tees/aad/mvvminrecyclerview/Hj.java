package uk.ac.tees.aad.mvvminrecyclerview;

public class Hj {

    private String name;
    private String info;
    private  int id;

    public Hj(String name, String info, int ID) {
        this.name = name;
        this.info = info;
        this.id = ID;
    }

    public String getName() {
        return name;
    }


    public String getInfo() {
        return info;
    }

    public int getId()
    {
        return id;
    }


}
